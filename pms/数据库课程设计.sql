create database pms;

use pms;

-- 1. 部门信息表 department
create table department (
    id int primary key,  -- 部门编号（主键）
    name varchar(50) not null,  -- 部门名称
    manager varchar(20),  -- 部门经理
    intro text  -- 部门简介
);

-- 2. 受教育程度代码表 edu_level
create table edu_level (
    code int primary key,  -- 受教育程度代码（主键）
    description varchar(20) not null  -- 描述（小学、初中等）
);

-- 3. 职务代码表 job
create table job (
    code int primary key,  -- 职务代码（主键）
    description varchar(20) not null  -- 描述（如经理、员工等）
);

-- 4. 人事变动代码表 personnel_change
create table personnel_change (
    code int primary key,  -- 人事变动代码（主键）
    description varchar(20) not null  -- 描述（如新员工加入等）
);

-- 5. 员工个人信息表 person
create table person (
    id int primary key,  -- 员工号（主键）
    password varchar(50) not null,  -- 密码
    authority varchar(20) not null,  -- 用户权限
    name varchar(20) not null,  -- 姓名
    sex char(2),  -- 性别
    birthday date,  -- 生日
    department int,  -- 所在部门（外键，关联department.id）
    job int,  -- 职务（外键，关联job.code）
    edu_level int,  -- 受教育程度（外键，关联edu_level.code）
    specialty varchar(50),  -- 专业
    address varchar(100),  -- 家庭住址
    tel varchar(20),  -- 联系电话
    email varchar(50),  -- 电子邮件
    state char(1) not null,  -- 当前状态（t=在职，f=非在职）
    remark text,  -- 备注
    foreign key (department) references department(id) on update cascade,
    foreign key (job) references job(code)on update cascade,
    foreign key (edu_level) references edu_level(code)on update cascade
);

-- 6. 人事变更记录表 personnel（修正：`change`加反引号）
create table personnel (
    id int  AUTO_INCREMENT primary key,  -- 记录编号（主键）
    person_id int not null,  -- 员工号（外键，关联person.id）
    person_name varchar(20), -- 员工姓名
    `change` int not null,  -- 变更代码（保留关键字，加反引号）
    description text,  -- 详细记录
    foreign key (person_id) references person(id)on update cascade,
    foreign key (`change`) references personnel_change(code)on update cascade
);

